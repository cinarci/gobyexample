package ornekler

import "fmt"

func degerler() {

	fmt.Println("Metinsel değerlerin kullanımları")
	fmt.Println("go" + "lang")

	fmt.Println("Sayısal değerlerin kullanımları")
	fmt.Println("1+1 =", 1+1)
	fmt.Println("7.0/3.0 =", 7.0/3.0)

	fmt.Println("Mantıksal değerlerin kullanımları")
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
}
